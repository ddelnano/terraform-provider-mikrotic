func TestUnmarshal(t *testing.T) {
	type testStruct struct {
		Name          string
		NotNamedOwner string `mikrotik:"owner"`
		RunCount      int    `mikrotik:"run-count"`
		Allowed       bool
		Schedule      types.MikrotikList
	}

	testCases := []struct {
		name           string
		expectedResult testStruct
		reply          routeros.Reply
	}{
		{
			name: "basic types only",
			reply: routeros.Reply{
				Re: []*proto.Sentence{
					{
						Word: "!re",
						List: []proto.Pair{
							{
								Key:   "name",
								Value: "testing script",
							},
							{
								Key:   "owner",
								Value: "admin",
							},
							{
								Key:   "run-count",
								Value: "3",
							},
							{
								Key:   "allowed",
								Value: "true",
							},
						},
					},
				},
			},
			expectedResult: testStruct{
				Name:          "testing script",
				NotNamedOwner: "admin",
				RunCount:      3,
				Allowed:       true,
			},
		},
		{
			name: "MikrotikList type",
			reply: routeros.Reply{
				Re: []*proto.Sentence{
					{
						Word: "!re",
						List: []proto.Pair{
							{
								Key:   "owner",
								Value: "admin",
							},
							{
								Key:   "schedule",
								Value: "mon,wed,fri",
							},
						},
					},
				},
			},
			expectedResult: testStruct{
				NotNamedOwner: "admin",
				Schedule:      []string{"mon", "wed", "fri"},
			},
		},
	}

	for _, tc := range testCases {
		t.Run(tc.name, func(t *testing.T) {
			targetStruct := testStruct{}
			err := Unmarshal(tc.reply, &targetStruct)
			assert.NoError(t, err)
			assert.Equal(t, tc.expectedResult, targetStruct)
		})
	}
}

func TestUnmarshalOnSlices(t *testing.T) {
	name := "testing script"
	owner := "admin"
	allowed := "true"
	type testStruct struct {
		Name          string
		NotNamedOwner string `mikrotik:"owner"`
		Allowed       bool
	}

	cases := []struct {
		name     string
		reply    routeros.Reply
		expected []testStruct
	}{
		{
			name: "reply with len > 1",
			reply: routeros.Reply{
				Re: []*proto.Sentence{
					{
						Word: "!re",
						List: []proto.Pair{
							{
								Key:   "name",
								Value: name,
							},
							{
								Key:   "owner",
								Value: owner,
							},
							{
								Key:   "allowed",
								Value: allowed,
							},
						},
					},
					{
						Word: "!re",
						List: []proto.Pair{
							{
								Key:   "name",
								Value: name + " 2",
							},
							{
								Key:   "owner",
								Value: owner + " 2",
							},
							{
								Key:   "allowed",
								Value: allowed,
							},
						},
					},
				},
			},
			expected: []testStruct{
				{
					Name:          name,
					NotNamedOwner: owner,
					Allowed:       true,
				},
				{
					Name:          name + " 2",
					NotNamedOwner: owner + " 2",
					Allowed:       true,
				},
			},
		},
		{
			name: "reply with single element",
			reply: routeros.Reply{
				Re: []*proto.Sentence{
					{
						Word: "!re",
						List: []proto.Pair{
							{
								Key:   "name",
								Value: name,
							},
							{
								Key:   "owner",
								Value: owner,
							},
							{
								Key:   "allowed",
								Value: allowed,
							},
						},
					},
				},
			},
			expected: []testStruct{
				{
					Name:          name,
					NotNamedOwner: owner,
					Allowed:       true,
				},
			},
		},
		{
			name: "empty reply",
			reply: routeros.Reply{
				Re: []*proto.Sentence{},
			},
			expected: []testStruct{},
		},
	}

	for _, tc := range cases {
		t.Run(tc.name, func(t *testing.T) {
			var result []testStruct
			err := Unmarshal(tc.reply, &result)

			if err != nil {
				t.Errorf("Failed to unmarshal with error: %v", err)
			}

			if !reflect.DeepEqual(tc.expected, result) {
				t.Errorf(`unexpected result:
				want: %#v
				got: %#v
				`, tc.expected, result)
			}
		})
	}
}

func TestMarshal(t *testing.T) {
	action := "/test/owner/add"
	testCases := []struct {
		name        string
		testStruct  interface{}
		expectedCmd []string
	}{
		{
			name: "basic types",
			testStruct: struct {
				Name          string `mikrotik:"name"`
				NotNamedOwner string `mikrotik:"owner,extraTagNotUsed"`
				RunCount      int    `mikrotik:"run-count"`
				ReadOnlyProp  bool   `mikrotik:"read-only-prop,readonly"`
				Allowed       bool   `mikrotik:"allowed-or-not"`
			}{
				Name:          "test owner",
				NotNamedOwner: "admin",
				RunCount:      3,
				Allowed:       true,
			},
			expectedCmd: []string{
				"/test/owner/add",
				"=name=test owner",
				"=owner=admin",
				"=run-count=3",
				"=allowed-or-not=yes",
			},
		},
		{
			name: "MikrotikList type",
			testStruct: struct {
				Name          string             `mikrotik:"name"`
				NotNamedOwner string             `mikrotik:"owner,extraTagNotUsed"`
				RunCount      int                `mikrotik:"run-count"`
				Allowed       bool               `mikrotik:"allowed-or-not"`
				Schedule      types.MikrotikList `mikrotik:"schedule"`
			}{
				Name:          "test owner",
				NotNamedOwner: "admin",
				RunCount:      3,
				Allowed:       true,
				Schedule:      []string{"mon", "tue", "fri"},
			},
			expectedCmd: []string{
				"/test/owner/add",
				"=name=test owner",
				"=owner=admin",
				"=run-count=3",
				"=allowed-or-not=yes",
				"=schedule=mon,tue,fri",
			},
		},
	}
	for _, tc := range testCases {
		t.Run(tc.name, func(t *testing.T) {
			cmd := Marshal(action, tc.testStruct)
			if !reflect.DeepEqual(cmd, tc.expectedCmd) {
				t.Errorf("Failed to marshal: %v does not equal expected %v", cmd, tc.expectedCmd)
			}
		})
	}
}

func TestMarshalStructWithoutTags(t *testing.T) {
	action := "/test/owner/add"
	name := "test owner"
	owner := "admin"
	runCount := 3
	allowed := true
	retain := false
	testStruct := struct {
		Name           string `example:"name"`
		NotNamedOwner  string `json:"not-named"`
		RunCount       int
		Allowed        bool
		Retain         bool
		SecondaryOwner string
	}{name, owner, runCount, allowed, retain, ""}

	expectedCmd := []string{action}
	cmd := Marshal(action, &testStruct)

	if !reflect.DeepEqual(cmd, expectedCmd) {
		t.Errorf("Marshaling with a struct without tags should return the command action supplied: %v does not equal expected %v", cmd, expectedCmd)
	}
}
